var celsiusTemp = 25;
console.log(celsiusTemp);
var convertCToF = (celsiusTemp * 9) / 5 + 32;
console.log(celsiusTemp + '\u00B0' + 'C is ' + convertCToF + '\u00B0' + 'F.');

var fahrenheitTemp = 77;
console.log(fahrenheitTemp);
var convertFToC = (fahrenheitTemp - 32) * 5 / 9;
console.log(fahrenheitTemp + '\u00B0' + 'C is ' + convertFToC + '\u00B0' + 'F.');